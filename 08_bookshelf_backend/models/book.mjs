import { Schema, model } from "mongoose";
import db from "../db/bookshelf.mjs";

const bookSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    rating: {
      type: Number,
      required: true,
    },
    comment: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Book = model('book', bookSchema);
export default Book;